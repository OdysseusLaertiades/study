package study.base.string.regex;

/**
 * Extract the first letter of each word in it. “Words” are defined as contiguous strings of alphabetic
 * characters i.e. any upper or lower case characters a-z or A-Z.
 *
 * collect("Lorem Ipsum") - LI
 * collect("Lorem 2zzz Ipsum") - LI
 *
 * Should be solved only with regular expressions
 */

public class FirstLetter {
  public String collect(String source) {
    String extractLetters = "";
    for (String word : source.split(" ")) {
      if(word.matches("[a-zA-Z]+")){
        extractLetters += word.charAt(0);
      }
    }
    return extractLetters;
  }
}
